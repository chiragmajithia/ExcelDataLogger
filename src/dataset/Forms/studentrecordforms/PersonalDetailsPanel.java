    private void jAddRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAddRemoveActionPerformed
        String s = jSchool.getEditor().getItem().toString();
        boolean removed = false;
        for (int i = 0; i < jSchool.getItemCount(); i++) {
            if (s.equalsIgnoreCase(jSchool.getItemAt(i))) {
                jSchool.removeItemAt(i);
                removed = true;
                break;
            }
        }
        if(!removed)
        jSchool.addItem(jSchool.getEditor().getItem().toString());        // TODO add your handling code here:
    }//GEN-LAST:event_jAddRemoveActionPerformed

